piesAmetros :: Float -> Float
piesAmetros pies = pies / 3.281

metrosApies :: Float -> Float
metrosApies metros = metros * 3.281

millasAkms :: Float -> Float
millasAkms millas = millas * 1.609

kmAmillas :: Float -> Float
kmAmillas km = km / 1.609

pulgadasAcm :: Float -> Float
pulgadasAcm pulgada = pulgada * 2.54

cmApulgada :: Float -> Float
cmApulgada cm = cm / 2.54